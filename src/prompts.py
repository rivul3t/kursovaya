import json

def build_teacher_prompt(task_id, topic, style, factor, difficulty_level, example=None):
    
    prompt = f"Ты — генератор экзаменационных вопросов в стиле {style}. Составь вопрос для задачи {task_id} на тему {topic}.\n"

    # Описание сложности
    if difficulty_level == "impossible":
        difficulty_desc = ("Создай исключительно тонкую аномалию, требующую глубоких знаний для выявления. "
                           "Аномалия должна быть концептуально нюансированной, а не фактической ошибкой, "
                           "естественно встроенной в текст. Задача должна быть сложна даже для экспертов, "
                           "но оставаться логически обоснованной.")
    elif difficulty_level == "extreme":
        difficulty_desc = ("Сделай аномалию крайне тонкой и сложной. "
                           "Для её обнаружения потребуются глубокое критическое мышление и экспертные знания. "
                           "Создай сложную задачу, трудную даже для продвинутых студентов.")
    elif difficulty_level == "hard":
        difficulty_desc = ("Сделай аномалию логически правдоподобной и контекстуально реалистичной, "
                           "но семантически или прагматически несогласованной. Избегай явных противоречий.")
    elif difficulty_level == "easy":
        difficulty_desc = ("Используй ясную, но не тривиальную аномалию. "
                           "Её можно обнаружить без глубокого критического анализа.")
    else:
        difficulty_desc = ("Создай нетривиальную аномалию, требующую внимательного чтения для выявления. "
                           "Она должна быть заметной, но не сразу очевидной.")

    # Инструкции по типу задачи
    if task_id == "T1":
        prompt += f"\nСгенерируй 5–6 предложений на тему {topic}. Одно из них должно быть аномальным (например, семантически некорректным или концептуально не по теме)."
        if factor:
            prompt += f" Аномалия должна быть связана с: {factor}."
        prompt += f" {difficulty_desc}"
        
        if example:
            prompt += f"\n\nПример формата:\n```json\n{json.dumps(example, ensure_ascii=False, indent=2)}\n```"
        
        prompt += f"\n\nВозврати результат строго в формате JSON:\n{{\n  \"context\": [\"...\"],\n  \"anomaly_index\": <integer>,\n  \"meta\": {{\n    \"source\": \"{style}\",\n    \"topic\": \"{topic}\",\n    \"anomaly_type\": \"{factor if factor else 'none'}\"\n  }}\n}}"

    elif task_id == "T2":
        prompt += f"\nСоздай 5 предложений о {topic} с учетом следующих условий:\n"
        prompt += "Случайным образом выбери ОДИН из вариантов:\n\n"
        
        prompt += "ВАРИАНТ 1: Напиши логически последовательные предложения в правильном порядке (is_coherent: true)\n\n"
        prompt += ("ВАРИАНТ 2: Напиши предложения с слегка нарушенным порядком (is_coherent: false):\n"
                   "   - Перемести 1–2 предложения, нарушив временной или причинно-следственный порядок\n"
                   "   - Избегай явной перестановки; сохраняй частичную локальную связность\n"
                   "   - Нарушение должно быть заметно, но не сразу очевидно\n\n")
        
        if example:
            prompt += f"\nПример формата:\n```json\n{json.dumps(example, ensure_ascii=False, indent=2)}\n```"
        
        prompt += "\n\nВозврати ТОЛЬКО один JSON с:\n{\n  \"context\": [список из 5 предложений],\n  \"is_coherent\": логическое (true, если порядок верный; false, если перемешано)\n}"

    elif task_id == "T3":
        prompt += f"\nСоставь задачу на завершение предложения на тему {topic}:\n"
        prompt += "1. Напиши одно предложение с пропуском (обозначен как ___)\n"
        prompt += "2. Предложи 5 вариантов, один из которых слегка неуместен\n"
        if factor:
            prompt += f"3. Неуместный вариант должен касаться: {factor}\n"
        prompt += f"4. {difficulty_desc}\n"
        
        if example:
            prompt += f"\nПример формата:\n```json\n{json.dumps(example, ensure_ascii=False, indent=2)}\n```"
        
        prompt += "\n\nВозврати JSON с:\n{\n  \"sentence\": \"предложение с ___\",\n  \"choices\": [5 вариантов],\n  \"anomaly_index\": индекс аномального варианта\n}"

    elif task_id == "T4":
        prompt += f"\nСоздай задачу на «мостовое предложение» на тему {topic}:\n"
        prompt += "1. Напиши два коротких абзаца (по 2–3 предложения каждый)\n"
        prompt += "2. Сгенерируй 5 вариантов мостовых предложений\n"
        prompt += "3. Одно предложение должно быть контекстуально или логически слабым\n"
        if factor:
            prompt += f"4. Слабое предложение должно содержать: {factor}\n"
        prompt += f"5. {difficulty_desc}\n"
        
        if example:
            prompt += f"\nПример формата:\n```json\n{json.dumps(example, ensure_ascii=False, indent=2)}\n```"
        
        prompt += "\n\nВозврати JSON с:\n{\n  \"paragraph_1\": [предложения],\n  \"paragraph_2\": [предложения],\n  \"bridges\": [5 вариантов мостов],\n  \"anomaly_index\": индекс слабого мостового предложения\n}"

    elif task_id == "T5":
        prompt += f"\nСоставь задачу на разрешение местоименной неоднозначности на тему {topic}:\n"
        prompt += "1. Напиши 5 предложений с местоимениями и их референтами\n"
        prompt += ("2. Одно предложение должно содержать неоднозначное местоимение, где непонятно, к какому существительному оно относится\n"
                   "3. Выбери только ОДИН тип неоднозначности и убедись, что он тонкий, но выявляемый\n"
                   "   a) местоимение с несколькими возможными референтами\n"
                   "   b) местоимение с неявным, но подразумеваемым референтом\n")
        prompt += "4. Сделай потенциальные референты семантически похожими или связанными, чтобы увеличить тонкость неоднозначности\n"
        if difficulty_level in ["hard", "extreme"]:
            prompt += "5. Убедись, что обнаружение неоднозначности требует внимательного анализа контекста\n"
        if difficulty_level == "extreme":
            prompt += ("6. Сделай многослойную неоднозначность с 2–3 возможными интерпретациями\n"
                       "7. Размести неоднозначное предложение в сложной структуре абзаца\n")
        if factor:
            prompt += f"8. Неоднозначность должна касаться: {factor}\n"
        prompt += f"9. {difficulty_desc}\n"
        if example:
            prompt += f"\nПример формата:\n```json\n{json.dumps(example, ensure_ascii=False, indent=2)}\n```"
        
        prompt += "\n\nВозврати JSON с:\n{\n  \"context\": [5 предложений],\n  \"anomaly_index\": индекс неоднозначного предложения\n}"

    elif task_id == "T6":
        prompt += f"\nСоздай задачу на логическое противоречие на тему {topic}:\n"
        prompt += "1. Сгенерируй 5 утверждений\n"
        prompt += "2. Одно из них должно содержать противоречие или обратную логику\n"
        if factor:
            prompt += f"3. Противоречие должно касаться: {factor}\n"
        prompt += f"4. {difficulty_desc}\n"
        if example:
            prompt += f"\nПример формата:\n```json\n{json.dumps(example, ensure_ascii=False, indent=2)}\n```"
        prompt += "\n\nВозврати JSON с:\n{\n  \"context\": [5 утверждений],\n  \"anomaly_index\": индекс противоречивого утверждения\n}"

    elif task_id == "T7":
        prompt += f"\nСоздай задачу на нарушение стиля/тона на тему {topic}:\n"
        prompt += "1. Напиши 5 предложений с единым формальным/академическим тоном\n"
        prompt += "2. Одно предложение должно слегка нарушать заданный тон/стиль\n"
        if factor:
            prompt += f"3. Нарушение должно касаться: {factor}\n"
        prompt += f"4. {difficulty_desc}\n"
        if example:
            prompt += f"\nПример формата:\n```json\n{json.dumps(example, ensure_ascii=False, indent=2)}\n```"
        prompt += "\n\nВозврати JSON с:\n{\n  \"context\": [5 предложений],\n  \"anomaly_index\": индекс предложения с нарушением тона\n}"

    return prompt